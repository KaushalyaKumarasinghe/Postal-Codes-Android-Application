package lk.icta.mobile.apps.postalcode.obj;

public class Divisions {
	private String[] divisionIds;
	private String[] divisionNames;
	private int count;

	public String[] getDivisionIds() {
		return divisionIds;
	}
	public void setDivisionIds(String[] divisionIds) {
		this.divisionIds = divisionIds;
	}
	public String[] getDivisionNames() {
		return divisionNames;
	}
	public void setDivisionNames(String[] divisionNames) {
		this.divisionNames = divisionNames;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
